package oprator;

public class Arithmatic {
	public static void main(String[] args) {
		
		int a = 10;
		int b = 20;
		
		System.out.println(a+b);
		System.out.println("Tops"+a);
		System.out.println("Tops"+a+b);
		System.out.println(a+b+"Tops");
		
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(13%2);
	}
}
